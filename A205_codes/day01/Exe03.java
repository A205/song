package com;

public class Exe03 {

	public static void main(String[] args) {
		int a=1,b=2;
		System.out.println("int:\n"+"a+b="+(a+b)+"  a-b="+(a-b)+"  a*b="+(a*b)+"  a/b="+(a/b));
		
		double c=1.0,d=2.0;
		System.out.println("double:\n"+"c+d="+(c+d)+"  c-d="+(c-d)+"  c*d="+(c*d)+"  c/d="+(c/d));
		
		char e='A',f='B';
		System.out.println("char:\n"+"e+f="+(e+f)+"  e-f="+(e-f)+"  e*f="+(e*f)+"  e/f="+(e/f));
	}

}
